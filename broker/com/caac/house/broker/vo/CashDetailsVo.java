package com.caac.house.broker.vo;

public class CashDetailsVo {
	private String id;
	private String extractCashId;
	private String moneryDetailsId;
	private String recommend;
	private int monery;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getExtractCashId() {
		return extractCashId;
	}

	public void setExtractCashId(String extractCashId) {
		this.extractCashId = extractCashId;
	}

	public String getMoneryDetailsId() {
		return moneryDetailsId;
	}

	public void setMoneryDetailsId(String moneryDetailsId) {
		this.moneryDetailsId = moneryDetailsId;
	}

	public String getRecommend() {
		return recommend;
	}

	public void setRecommend(String recommend) {
		this.recommend = recommend;
	}

	public int getMonery() {
		return monery;
	}

	public void setMonery(int monery) {
		this.monery = monery;
	}

}
